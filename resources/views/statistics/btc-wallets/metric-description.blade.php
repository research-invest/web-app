<div class="bg-light p-3 mb-3 rounded"><h4>Описание метрик</h4>
    <ul>
        <li><strong>Whale Score</strong> - Оценка влияния кошелька в сети. Высокие значения указывают на то, что это
            может быть "кит" (крупный держатель BTC).
        </li>
        <li><strong>Momentum</strong> - Показатель импульса/динамики активности. Отражает направление и скорость
            изменения активности кошелька.
        </li>
        <li><strong>Correlation</strong> - Корреляция активности кошелька с движениями рынка. Положительные значения
            означают, что активность совпадает с трендами рынка.
        </li>
        <li><strong>Smart Index</strong> - Индекс "умности" кошелька. Оценивает насколько успешными были прошлые
            транзакции кошелька.
        </li>
        <li><strong>Stability</strong> - Показатель стабильности кошелька. Высокие значения означают стабильный паттерн
            использования.
        </li>
    </ul>
</div>
